

//사칙연산 
a = 7;
b = 3;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(parseInt(a / b)); // 몫만 남기기
console.log(a % b);

//할당 연산자
console.log('a+=b => '+`${a+=b}`);
//a==10 b==3
console.log('a-=b => '+`${a-=b}`);
//a==7 b==3
console.log('a*=b => '+`${a*=b}`);
//a==21 b==3
console.log('a/=b => '+`${a/=b}`);
//a==7 b==3